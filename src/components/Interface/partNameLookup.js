export const partNameLookup = (value) => {
  switch (value) {
    case 0:
      return 'outer swoosh';

    case 1:
      return 'inner swoosh';

    case 2:
      return 'outer quarter';

    case 3:
      return 'inner quarter';

    case 4:
      return 'outer heel';

    case 5:
      return 'inner heel';

    case 6:
      return 'outer sole';

    case 7:
      return 'inner sole';

    case 8:
      return 'sole';

    case 9:
      return 'toe box';

    case 10:
      return 'toe cap';

    case 11:
      return 'lace';

    case 12:
      return 'lace cage';

    case 13:
      return 'lace lock';

    case 14:
      return 'tongue';

    case 15:
      return 'heel wing';

    case 16:
      return 'heel tab';

    case 17:
      return 'sock liner';

    default:
  }
};
